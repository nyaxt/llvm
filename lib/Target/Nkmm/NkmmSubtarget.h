#ifndef LLVM_LIB_TARGET_NKMM_NKMMSUBTARGET_H
#define LLVM_LIB_TARGET_NKMM_NKMMSUBTARGET_H

#include "NkmmISelLowering.h"
#include "NkmmInstrInfo.h"
#include "NkmmFrameLowering.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"
#include "llvm/Support/ErrorHandling.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "NkmmGenSubtargetInfo.inc"

namespace llvm {
class StringRef;
class NkmmTargetMachine;

class NkmmSubtarget : public NkmmGenSubtargetInfo {
  virtual void anchor();

  const NkmmTargetMachine &TM;
  NkmmInstrInfo InstrInfo;
  NkmmFrameLowering FrameLowering;
  NkmmTargetLowering TLInfo;
  const DataLayout DL; // Calculates type size & alignment
public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  NkmmSubtarget(const std::string &TT, const std::string &CPU,
                const std::string &FS, const NkmmTargetMachine &TM);

  const NkmmRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const NkmmInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const NkmmTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const DataLayout *getDataLayout() const override { return &DL; }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};

} // End llvm namespace

#endif
